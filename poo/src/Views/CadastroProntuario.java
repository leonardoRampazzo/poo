    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        if(!SelecaoPaciente.selecaoPacienteFechado) return;
        int codigoPac = 0;
        codigoPac = new SelecaoPaciente(this, true).getCodigo();
        
        if(codigoPac==0) return;
        selPaciente = ControlePaciente.retornaPaciente(codigoPac);
        jTextField4.setText(selPaciente.getNome());
    }//GEN-LAST:event_jButton7ActionPerformed

