    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        selMedico = ControleInicial.usuarioLogado.getMedico();
        
        if(selMedico==null) {
            JOptionPane.showMessageDialog(rootPane, "Para Cadastrar Prontuário deve-se estar logado como Médico", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if(!ControleProntuario.validaProntuario(jTextField1.getText(), jTextField2.getText(), selMedico, selPaciente, jTextArea1.getText(),jTextArea2.getText(), jTextArea3.getText())){
           JOptionPane.showMessageDialog(rootPane, "Reveja os Campos de Cadastro/Alteração", "Atenção", JOptionPane.WARNING_MESSAGE);
           return;
        }
        
        
        if (inserindo) ControleProntuario.InserirProntuario(jTextField1.getText(), jTextField2.getText(), selMedico, selPaciente, jTextArea1.getText(),jTextArea2.getText(), jTextArea3.getText());
        else ControleProntuario.EditarProntuario(Integer.parseInt(jTextField9.getText()), jTextField1.getText(), jTextField2.getText(),ControleInicial.usuarioLogado.getMedico(),selPaciente, jTextArea1.getText(),jTextArea2.getText(), jTextArea3.getText());             
        //System.out.println("aqui");
        
        this.jTabbedPane2.setSelectedIndex(0);
        LimpaCampos();
        CarregaTabela();
    }//GEN-LAST:event_jButton1ActionPerformed

