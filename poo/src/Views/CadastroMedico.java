    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!ControleMedico.validaMedico(jTextField1.getText(), jTextField2.getText())){
           JOptionPane.showMessageDialog(rootPane, "Reveja os Campos de Cadastro/Alteração", "Atenção", JOptionPane.WARNING_MESSAGE);
           return;
        }
        
        if (inserindo) ControleMedico.InserirMedico(jTextField1.getText(), jTextField2.getText() );
        else ControleMedico.EditarMedico(Integer.parseInt(jTextField9.getText()), jTextField1.getText(), jTextField2.getText() );
        this.jTabbedPane2.setSelectedIndex(0);
        LimpaCampos();
        CarregaTabela();
    }//GEN-LAST:event_jButton1ActionPerformed

