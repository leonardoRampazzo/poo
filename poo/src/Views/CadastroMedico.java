    private void CarregaTabela(){
        String filtroNome;
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        
        TypedQuery<Medico> query = manager.createNamedQuery("Medico.findAll", Medico.class);
        List<Medico> medicos = query.getResultList();
        
        if (!medicos.isEmpty()) {
            filtroNome = jTextField13.getText();
            
            for (Medico medico : medicos) {
              
                if(medico.isInativo()) continue;
                if(medico.getNome().toLowerCase().contains(filtroNome.toLowerCase()))
                modelo.addRow( new String [] { Integer.toString(medico.getCodigo()), medico.getNome(), medico.getAtuacao()} );
                
           }
       }
    }
    
    private void PreencheDados(Medico pMedico){
       jTabbedPane2.setSelectedIndex(1);       
       jTextField9.setText(Integer.toString(pMedico.getCodigo()));
       jTextField1.setText(pMedico.getNome());
       jTextField2.setText(pMedico.getAtuacao());
    }
    
    private void LimpaCampos(){
       jTextField13.setText(""); 
       jTextField9.setText("");
       jTextField1.setText("");
       jTextField2.setText("");
    }
    
