    private void CarregaTabela() {
        String filtroData;
        String filtroHorario;
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);

        TypedQuery<Consulta> query = manager.createNamedQuery("Consulta.findAll", Consulta.class);
        List<Consulta> consultas = query.getResultList();

        if (!consultas.isEmpty()) {
            filtroData = jTextField6.getText();
            filtroHorario = jTextField7.getText();
            for (Consulta consulta : consultas) {
                if (consulta.isInativo()) {
                    continue;
                }

                if (!consulta.getData().contains(filtroData.trim())) {
                    continue;
                }
                if (!consulta.getHorario().contains(filtroHorario)) {
                    continue;
                }

                modelo.addRow(new String[]{Integer.toString(consulta.getCodigo()), consulta.getData(), consulta.getHorario(), consulta.getMedico().getNome(), consulta.getPaciente().getNome(), consulta.getTipo()});
            }
        }
    }

    private void PreencheDados(Consulta pConsulta) {
        jTabbedPane2.setSelectedIndex(1);
        jTextField9.setText(Integer.toString(pConsulta.getCodigo()));
        jTextField1.setText(pConsulta.getData());
        jTextField2.setText(pConsulta.getHorario());
        jComboBox3.setSelectedItem(pConsulta.getTipo());
        jTextField3.setText(pConsulta.getMedico().getNome());
        jTextField4.setText(pConsulta.getPaciente().getNome());
    }

