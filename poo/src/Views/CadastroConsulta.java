    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:  
        if (!SelecaoMedico.selecaoMedicoFechado) {
            return;
        }
        int codigoMedico = 0;
        codigoMedico = new SelecaoMedico(this, true).getCodigo();
        if (codigoMedico == 0) {
            return;
        }

        selMedico = ControleMedico.retornaMedico(codigoMedico);
        jTextField3.setText(selMedico.getNome());
        
        
    }//GEN-LAST:event_jButton5ActionPerformed

