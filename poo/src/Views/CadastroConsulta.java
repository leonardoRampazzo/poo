    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:  
        if (!ControleConsulta.validaConsulta(jTextField1.getText(), jTextField2.getText(), jComboBox3.getSelectedItem().toString(), selMedico, selPaciente)) {
            JOptionPane.showMessageDialog(rootPane, "Reveja os Campos de Cadastro/Alteração", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (inserindo) {
            ControleConsulta.InserirConsulta(jTextField1.getText(), jTextField2.getText(), jComboBox3.getSelectedItem().toString(), selMedico, selPaciente);
        } else {
            ControleConsulta.EditarConsulta(Integer.parseInt(jTextField9.getText()), jTextField1.getText(), jTextField2.getText(), jComboBox3.getSelectedItem().toString(), selMedico, selPaciente);
        }

        this.jTabbedPane2.setSelectedIndex(0);
        LimpaCampos();
        CarregaTabela();
    }//GEN-LAST:event_jButton1ActionPerformed

